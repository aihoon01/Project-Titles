const kelvin = 0
//Temperature in kelvin is constant
const celsius = kelvin - 273
// converting kelvin to celsius
let fahrenheit = celsius * (9/5) + 32
// converting celsius to fahrenheit
fahrenheit = Math.floor(fahrenheit)
// rounding fahrenheit to whole numbers.
console.log(`The temperature is ${fahrenheit} in degrees Fahrenheit.`)
let newton = celsius * (33/100)
//converting celsius to newton
newton = Math.floor(newton)
//rounding newton to whole numbers
console.log(`The temperature is ${newton} in newton`)